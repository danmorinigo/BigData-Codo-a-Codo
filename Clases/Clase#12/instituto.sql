CREATE DATABASE IF NOT EXISTS instituto DEFAULT CHARACTER SET utf8 COLLATE utf8_spanish_ci;
USE instituto;


CREATE TABLE genero (
  genero_id int(11) AUTO_INCREMENT,
  genero varchar(11) DEFAULT NULL,
  PRIMARY KEY (genero_id)
);


CREATE TABLE nivel (
  nivel_id int(11) AUTO_INCREMENT,
  nivel varchar(5) DEFAULT NULL,
  PRIMARY KEY (nivel_id)
);


CREATE TABLE instructores (
  instructor_id int(11) AUTO_INCREMENT,
  apellido varchar(20) DEFAULT NULL,
  nombre varchar(20) DEFAULT NULL,
  email varchar(50) DEFAULT NULL,
  PRIMARY KEY (instructor_id)
);


CREATE TABLE alumnos (
  legajo int(11) AUTO_INCREMENT,
  nombre varchar(20) DEFAULT NULL,
  apellido varchar(20) DEFAULT NULL,
  email varchar(50) DEFAULT NULL,
  genero_id int(11) DEFAULT NULL,
  nota_1p decimal(4,2) DEFAULT NULL,
  nota_2p decimal(4,2) DEFAULT NULL,
  nota_final decimal(4,2) DEFAULT NULL,
  nivel_id int(11) DEFAULT NULL,
  instructor_id int(11) DEFAULT NULL,
  PRIMARY KEY (legajo),
  FOREIGN KEY (genero_id) REFERENCES genero (genero_id),
  FOREIGN KEY (nivel_id) REFERENCES nivel (nivel_id),
  FOREIGN KEY (instructor_id) REFERENCES instructores (instructor_id)
);


